{
    /**
     * constructor
     *
     * @access public
     * @return void
     * @author Ultimate Module Creator
     */
    public function __construct()
    {
        $this->_objectId    = 'entity_id';
        $this->_blockGroup  = 'myextension_mymodule';
        $this->_controller  = 'adminhtml_articulo';
        $this->_mode        = 'edit';
