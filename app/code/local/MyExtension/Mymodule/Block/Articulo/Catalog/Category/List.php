/**
 * Articulo category list
 *
 * @category    MyExtension
 * @package     MyExtension_Mymodule
 * @author      Ultimate Module Creator
 */
class MyExtension_Mymodule_Block_Articulo_Catalog_Category_List extends Mage_Core_Block_Template
{
    /**
     * get the list of products
     *
     * @access public
     * @return Mage_Catalog_Model_Resource_Category_Collection
     * @author Ultimate Module Creator
     */
    public function getCategoryCollection()
    {
        $collection = $this->getArticulo()->getSelectedCategoriesCollection();
        $collection->addAttributeToSelect('name');
        $collection->getSelect()->order('related.position');
        $collection->addAttributeToFilter('is_active', 1);
        return $collection;
    }

    /**
     * get current articulo
     *
     * @access public
     * @return MyExtension_Mymodule_Model_Articulo
     * @author Ultimate Module Creator
     */
    public function getArticulo()
    {
        return Mage::registry('current_articulo');
    }
}
